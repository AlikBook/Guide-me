def get_station_coordinates_with_mapping():
    """Get all station coordinates with proper mapping to handle duplicates"""
    # Create a mapping that preserves both metro and RER stations with same names
    all_coords = {}
    
    # Add metro stations
    for station, coords in METRO_STATION_COORDINATES.items():
        all_coords[station] = {
            "coordinates": coords,
            "type": "metro",
            "latitude": coords[0],
            "longitude": coords[1]
        }
    
    # Add RER stations, handling duplicates by creating separate entries
    for station, coords in RER_STATION_COORDINATES.items():
        if station in all_coords:
            # Station exists in metro, create RER version with suffix
            all_coords[f"{station} (RER)"] = {
                "coordinates": coords,
                "type": "rer", 
                "latitude": coords[0],
                "longitude": coords[1],
                "original_name": station
            }
        else:
            # Station doesn't exist in metro, add normally
            all_coords[station] = {
                "coordinates": coords,
                "type": "rer",
                "latitude": coords[0], 
                "longitude": coords[1]
            }
    
    return all_coords

def get_station_coordinates_by_line_type(station_name, line_key):
    """Get coordinates for a station based on the line type"""
    # Determine if it's metro or RER based on line key
    if line_key.upper().startswith('METRO') or line_key.isdigit():
        # Metro line
        return METRO_STATION_COORDINATES.get(station_name)
    elif line_key.upper().startswith('RER') or line_key.upper() in ['A', 'B', 'C', 'D', 'E']:
        # RER line
        return RER_STATION_COORDINATES.get(station_name)
    else:
        # Try both, prioritize metro
        coords = METRO_STATION_COORDINATES.get(station_name)
        if coords is None:
            coords = RER_STATION_COORDINATES.get(station_name)
        return coords

